//        GLES20.glUniform1f(getHandle("saturation"), saturation)
//        GLES20.glUniform1f(getHandle("hue"), (hue * Math.PI / 180f).toFloat())
//        GLES20.glUniform1f(getHandle("highlights"), highlights)
//        GLES20.glUniform1f(getHandle("shadows"), shadows)
//        GLES20.glUniform1f(getHandle("vignette"), vignette)
//        GLES20.glUniform1f(getHandle("sharpen"), sharpen)
//        GLES20.glUniform1f(getHandle("grain"), grain)



    }

    fun destroy() {
        GLES20.glDeleteProgram(program)
        onDestroy()
    }

    fun onDestroy() {}

    companion object {
        private val TAG = GlFilter::class.java.simpleName
        private const val FLOAT_SIZE_BYTES = 4
        private const val TRIANGLE_VERTICES_DATA_STRIDE_BYTES =
            5 * FLOAT_SIZE_BYTES
        private const val TRIANGLE_VERTICES_DATA_POS_OFFSET = 0
        private const val TRIANGLE_VERTICES_DATA_UV_OFFSET = 3
    }

    init {
        mRunOnDraw = LinkedList()
        val a = ByteBuffer.allocateDirect(32)
        a.order(ByteOrder.nativeOrder())
        mVerBuffer = a.asFloatBuffer()
        mVerBuffer.put(pos)
        mVerBuffer.position(0)
        val b = ByteBuffer.allocateDirect(32)
        b.order(ByteOrder.nativeOrder())
        mTexBuffer = b.asFloatBuffer()
        mTexBuffer.put(coord)
        mTexBuffer.position(0)
    }
}