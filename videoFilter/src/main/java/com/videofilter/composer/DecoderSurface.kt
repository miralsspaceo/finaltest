package com.videofilter.composer

import android.graphics.SurfaceTexture
import android.graphics.SurfaceTexture.OnFrameAvailableListener
import android.opengl.EGL14
import android.opengl.Matrix
import android.util.Log
import android.view.Surface
import com.videofilter.OpenGlUtils.checkGlError
import com.videofilter.Resolution
import com.videofilter.filters.GlFilter
import java.util.concurrent.TimeUnit
import java.util.concurrent.locks.ReentrantLock
import kotlin.concurrent.withLock

internal class DecoderSurface(private var filter: GlFilter?) : OnFrameAvailableListener {
    private var eglDisplay = EGL14.EGL_NO_DISPLAY
    private var eglContext = EGL14.EGL_NO_CONTEXT
    private var eglSurface = EGL14.EGL_NO_SURFACE
    private var surfaceTexture: SurfaceTexture? = null

    /**
     * Returns the Surface that we draw onto.
     */
    var surface: Surface? = null
        private set
    private val frameSyncObject = ReentrantLock()
    private val condition = frameSyncObject.newCondition()
    private var frameAvailable = false
    private val MVPMatrix = FloatArray(16)
    private val STMatrix = FloatArray(16)
    private var rotation = Rotation.NORMAL
    private var outputResolution: Resolution? = null
    private var inputResolution: Resolution? = null
    private var fillMode = FillMode.PRESERVE_ASPECT_FIT
    private var fillModeCustomItem: FillModeCustomItem? = null
    private var flipVertical = false
    private var flipHorizontal = false

    /**
     * Creates instances of TextureRender and SurfaceTexture, and a Surface associated
     * with the SurfaceTexture.
     */
    private fun setup() {

        // Even if we don't access the SurfaceTexture after the constructor returns, we
        // still need to keep a reference to it.  The Surface doesn't retain a reference
        // at the Java level, so if we don't either then the object can get GCed, which
        // causes the native finalizer to run.
        if (VERBOSE) Log.d(TAG, "textureID=" + filter!!.textureId)
        surfaceTexture = SurfaceTexture(filter!!.textureId)
        // This doesn't work if DecoderSurface is created on the thread that CTS started for
        // these test cases.
        //
        // The CTS-created thread has a Looper, and the SurfaceTexture constructor will
        // create a Handler that uses it.  The "frame available" message is delivered
        // there, but since we're not a Looper-based thread we'll never see it.  For
        // this to do anything useful, DecoderSurface must be created on a thread without
        // a Looper, so that SurfaceTexture uses the main application Looper instead.
        //
        // Java language note: passing "this" out of a constructor is generally unwise,
        // but we should be able to get away with it here.
        surfaceTexture!!.setOnFrameAvailableListener(this)
        surface = Surface(surfaceTexture)
        Matrix.setIdentityM(STMatrix, 0)
    }

    /**
     * Discard all resources held by this class, notably the EGL context.
     */
    fun release() {
        if (eglDisplay !== EGL14.EGL_NO_DISPLAY) {
            EGL14.eglDestroySurface(eglDisplay, eglSurface)
            EGL14.eglDestroyContext(eglDisplay, eglContext)
            EGL14.eglReleaseThread()
            EGL14.eglTerminate(eglDisplay)
        }
        surface!!.release()
        // this causes a bunch of warnings that appear harmless but might confuse someone:
        //  W BufferQueue: [unnamed-3997-2] cancelBuffer: BufferQueue has been abandoned!
        //surfaceTexture.release();
        eglDisplay = EGL14.EGL_NO_DISPLAY
        eglContext = EGL14.EGL_NO_CONTEXT
        eglSurface = EGL14.EGL_NO_SURFACE
        filter!!.release()
        filter = null
        surface = null
        surfaceTexture = null
    }

    /**
     * Latches the next buffer into the texture.  Must be called from the thread that created
     * the DecoderSurface object, after the onFrameAvailable callback has signaled that new
     * data is available.
     */
    fun awaitNewImage() {
        val TIMEOUT_MS = 10000L
//        synchronized(frameSyncObject) {
        frameSyncObject.withLock {
            while (!frameAvailable) {
                try {
                    // Wait for onFrameAvailable() to signal us.  Use a timeout to avoid
                    // stalling the test if it doesn't arrive.

//                    frameSyncObject.await(TIMEOUT_MS.toLong())
                    condition.await(TIMEOUT_MS, TimeUnit.MILLISECONDS)
                    if (!frameAvailable) {
                        throw RuntimeException("Surface frame wait timed out")
                    }
                } catch (ie: InterruptedException) {
                    // shouldn't happen
                    throw RuntimeException(ie)
                }
            }
            frameAvailable = false
        }
        // Latch the data.
        checkGlError("before updateTexImage")
        surfaceTexture!!.updateTexImage()
    }

    /**
     * Draws the data from SurfaceTexture onto the current EGL surface.
     */
    fun drawImage() {
        Matrix.setIdentityM(MVPMatrix, 0)
        val scaleDirectionX: Float = if (flipHorizontal) -1f else 1.toFloat()
        val scaleDirectionY: Float = if (flipVertical) -1f else 1.toFloat()
        val scale: FloatArray
        when (fillMode) {
            FillMode.PRESERVE_ASPECT_FIT -> {
                scale = FillMode.Companion.getScaleAspectFit(
                    rotation.rotation,
                    inputResolution!!.width(),
                    inputResolution!!.height(),
                    outputResolution!!.width(),
                    outputResolution!!.height()
                )
                Matrix
                    .scaleM(
                        MVPMatrix,
                        0,
                        scale[0] * scaleDirectionX,
                        scale[1] * scaleDirectionY,
                        1f
                    )
                if (rotation != Rotation.NORMAL) {
                    Matrix.rotateM(MVPMatrix, 0, -rotation.rotation.toFloat(), 0f, 0f, 1f)
                }
            }
            FillMode.PRESERVE_ASPECT_CROP -> {
                scale = FillMode.Companion.getScaleAspectCrop(
                    rotation.rotation,
                    inputResolution!!.width(),
                    inputResolution!!.height(),
                    outputResolution!!.width(),
                    outputResolution!!.height()
                )
                Matrix
                    .scaleM(
                        MVPMatrix,
                        0,
                        scale[0] * scaleDirectionX,
                        scale[1] * scaleDirectionY,
                        1f
                    )
                if (rotation != Rotation.NORMAL) {
                    Matrix.rotateM(MVPMatrix, 0, -rotation.rotation.toFloat(), 0f, 0f, 1f)
                }
            }
            FillMode.CUSTOM -> if (fillModeCustomItem != null) {
                Matrix.translateM(
                    MVPMatrix,
                    0,
                    fillModeCustomItem!!.translateX,
                    -fillModeCustomItem!!.translateY,
                    0f
                )
                scale = FillMode.Companion.getScaleAspectCrop(
                    rotation.rotation,
                    inputResolution!!.width(),
                    inputResolution!!.height(),
                    outputResolution!!.width(),
                    outputResolution!!.height()
                )
                if (fillModeCustomItem!!.rotate == 0f || fillModeCustomItem!!.rotate == 180f) {
                    Matrix.scaleM(
                        MVPMatrix,
                        0,
                        fillModeCustomItem!!.scale * scale[0] * scaleDirectionX,
                        fillModeCustomItem!!.scale * scale[1] * scaleDirectionY, 1f
                    )
                } else {
                    Matrix.scaleM(
                        MVPMatrix,
                        0,
                        fillModeCustomItem!!.scale * scale[0] * (1 / fillModeCustomItem!!.videoWidth * fillModeCustomItem!!.videoHeight) * scaleDirectionX,
                        fillModeCustomItem!!.scale * scale[1] * (fillModeCustomItem!!.videoWidth / fillModeCustomItem!!.videoHeight) * scaleDirectionY,
                        1f
                    )
                }
                Matrix.rotateM(
                    MVPMatrix,
                    0,
                    -(rotation.rotation + fillModeCustomItem!!.rotate),
                    0f,
                    0f,
                    1f
                )

//                    Log.d(TAG, "inputResolution = " + inputResolution.width() + " height = " + inputResolution.height());
//                    Log.d(TAG, "out = " + outputResolution.width() + " height = " + outputResolution.height());
//                    Log.d(TAG, "rotation = " + rotation.getRotation());
//                    Log.d(TAG, "scale[0] = " + scale[0] + " scale[1] = " + scale[1]);
            }
            else -> {
            }
        }
        filter!!.draw(surfaceTexture, STMatrix, MVPMatrix)
    }

    override fun onFrameAvailable(st: SurfaceTexture) {
        if (VERBOSE) Log.d(TAG, "new frame available")
//        synchronized(frameSyncObject) {
        frameSyncObject.withLock {
            if (frameAvailable) {
                throw RuntimeException("frameAvailable already set, frame could be dropped")
            }
            frameAvailable = true
//            frameSyncObject.notifyAll()
            condition.signalAll()
        }
    }

    fun setRotation(rotation: Rotation) {
        this.rotation = rotation
    }

    fun setOutputResolution(resolution: Resolution?) {
        outputResolution = resolution
    }

    fun setFillMode(fillMode: FillMode) {
        this.fillMode = fillMode
    }

    fun setInputResolution(resolution: Resolution?) {
        inputResolution = resolution
    }

    fun setFillModeCustomItem(fillModeCustomItem: FillModeCustomItem?) {
        this.fillModeCustomItem = fillModeCustomItem
    }

    fun setFlipVertical(flipVertical: Boolean) {
        this.flipVertical = flipVertical
    }

    fun setFlipHorizontal(flipHorizontal: Boolean) {
        this.flipHorizontal = flipHorizontal
    }

    companion object {
        private const val TAG = "DecoderSurface"
        private const val VERBOSE = false
    }

    /**
     * Creates an DecoderSurface using the current EGL context (rather than establishing a
     * new one).  Creates a Surface that can be passed to MediaCodec.configure().
     */
    init {
        filter!!.setUpSurface()
        setup()
    }
}