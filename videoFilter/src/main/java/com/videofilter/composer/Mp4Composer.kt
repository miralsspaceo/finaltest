package com.videofilter.composer

import android.media.MediaMetadataRetriever
import android.util.Log
import com.videofilter.Resolution
import com.videofilter.filters.GlFilter
import com.videofilter.filters.IResolutionFilter
import java.io.File
import java.io.FileInputStream
import java.io.FileNotFoundException
import java.io.IOException
import java.util.concurrent.ExecutorService
import java.util.concurrent.Executors

class Mp4Composer (private val srcPath: String, private val destPath: String) {
    private var filter: GlFilter? = null
    private var outputResolution: Resolution? = null
    private var bitrate = -1
    private var mute = false
    private var rotation = Rotation.NORMAL
    private var listener: Listener? = null
    private var fillMode: FillMode? = FillMode.PRESERVE_ASPECT_FIT
    private var fillModeCustomItem: FillModeCustomItem? = null
    private var timeScale = 1
    private var flipVertical = false
    private var flipHorizontal = false
    private var executorService: ExecutorService? = null
    fun filter(filter: GlFilter): Mp4Composer {
        this.filter = filter
        return this
    }

    fun size(width: Int, height: Int): Mp4Composer {
        outputResolution = Resolution(width, height)
        return this
    }

    fun videoBitrate(bitrate: Int): Mp4Composer {
        this.bitrate = bitrate
        return this
    }

    fun mute(mute: Boolean): Mp4Composer {
        this.mute = mute
        return this
    }

    fun flipVertical(flipVertical: Boolean): Mp4Composer {
        this.flipVertical = flipVertical
        return this
    }

    fun flipHorizontal(flipHorizontal: Boolean): Mp4Composer {
        this.flipHorizontal = flipHorizontal
        return this
    }

    fun rotation(rotation: Rotation): Mp4Composer {
        this.rotation = rotation
        return this
    }

    fun fillMode(fillMode: FillMode): Mp4Composer {
        this.fillMode = fillMode
        return this
    }

    fun customFillMode(fillModeCustomItem: FillModeCustomItem): Mp4Composer {
        this.fillModeCustomItem = fillModeCustomItem
        fillMode = FillMode.CUSTOM
        return this
    }

    fun listener(listener: Listener): Mp4Composer {
        this.listener = listener
        return this
    }

    fun timeScale(timeScale: Int): Mp4Composer {
        this.timeScale = timeScale
        return this
    }

    private fun getExecutorService(): ExecutorService? {
        if (executorService == null) {
            executorService = Executors.newSingleThreadExecutor()
        }
        return executorService
    }

    fun start(): Mp4Composer {
        getExecutorService()!!.execute(Runnable {
            val engine = Mp4ComposerEngine()
            engine.setProgressCallback(object : Mp4ComposerEngine.ProgressCallback {
                override fun onProgress(progress: Double) {
                    if (listener != null) {
                        listener!!.onProgress(progress)
                    }
                }

            })
            val srcFile = File(srcPath)
            val fileInputStream: FileInputStream
            fileInputStream = try {
                FileInputStream(srcFile)
            } catch (e: FileNotFoundException) {
                e.printStackTrace()
                if (listener != null) {
                    listener!!.onFailed(e)
                }
                return@Runnable
            }
            try {
                engine.setDataSource(fileInputStream.fd)
            } catch (e: IOException) {
                e.printStackTrace()
                if (listener != null) {
                    listener!!.onFailed(e)
                }
                return@Runnable
            }
            val videoRotate = getVideoRotation(srcPath)
            val srcVideoResolution = getVideoResolution(srcPath, videoRotate)
            if (filter == null) {
                filter = GlFilter()
            }
            if (fillMode == null) {
                fillMode = FillMode.PRESERVE_ASPECT_FIT
            }
            if (fillModeCustomItem != null) {
                fillMode = FillMode.CUSTOM
            }
            if (outputResolution == null) {
                outputResolution = if (fillMode == FillMode.CUSTOM) {
                    srcVideoResolution
                } else {
                    val rotate: Rotation =
                        Rotation.Companion.fromInt(rotation.rotation + videoRotate)
                    if (rotate == Rotation.ROTATION_90 || rotate == Rotation.ROTATION_270) {
                        Resolution(srcVideoResolution.height(), srcVideoResolution.width())
                    } else {
                        srcVideoResolution
                    }
                }
            }
            if (filter is IResolutionFilter) {
                (filter as IResolutionFilter).setResolution(outputResolution!!)
            }
            if (timeScale < 2) {
                timeScale = 1
            }
            Log.d(TAG, "rotation = " + (rotation.rotation + videoRotate))
            Log.d(
                TAG,
                "inputResolution width = " + srcVideoResolution.width() + " height = " + srcVideoResolution.height()
            )
            Log.d(
                TAG,
                "outputResolution width = " + outputResolution!!.width() + " height = " + outputResolution!!.height()
            )
            Log.d(TAG, "fillMode = $fillMode")
            try {
                if (bitrate < 0) {
                    bitrate = calcBitRate(outputResolution!!.width(), outputResolution!!.height())
                }
                engine.compose(
                    destPath,
                    outputResolution,
                    filter,
                    bitrate,
                    mute,
                    Rotation.Companion.fromInt(rotation.rotation + videoRotate),
                    srcVideoResolution,
                    fillMode!!,
                    fillModeCustomItem,
                    timeScale,
                    flipVertical,
                    flipHorizontal
                )
            } catch (e: Exception) {
                e.printStackTrace()
                if (listener != null) {
                    listener!!.onFailed(e)
                }
                executorService!!.shutdown()
                return@Runnable
            }
            if (listener != null) {
                listener!!.onCompleted()
            }
            executorService!!.shutdown()
        })
        return this
    }

    fun cancel() {
        getExecutorService()!!.shutdownNow()
    }

    interface Listener {
        /**
         * Called to notify progress.
         *
         * @param progress Progress in [0.0, 1.0] range, or negative value if progress is unknown.
         */
        fun onProgress(progress: Double)

        /**
         * Called when transcode completed.
         */
        fun onCompleted()

        /**
         * Called when transcode canceled.
         */
        fun onCanceled()
        fun onFailed(exception: Exception?)
    }

    private fun getVideoRotation(videoFilePath: String): Int {
        val mediaMetadataRetriever = MediaMetadataRetriever()
        try {
            mediaMetadataRetriever.setDataSource(videoFilePath)
        } catch (e: Exception) {
            Log.e("MediaMetadataRetriever", "getVideoRotation \$orientation")
            return 0
        }
        val orientation = mediaMetadataRetriever.extractMetadata(
            MediaMetadataRetriever.METADATA_KEY_VIDEO_ROTATION
        )
        Log.e("MediaMetadataRetriever", "getVideoRotation orientation$orientation")
        return Integer.valueOf(orientation)
    }

    private fun calcBitRate(width: Int, height: Int): Int {
        val bitrate = (0.25 * 30 * width * height).toInt()
        Log.i(TAG, "bitrate=$bitrate")
        return bitrate
    }

    private fun getVideoResolution(path: String, rotation: Int): Resolution {
        val retriever = MediaMetadataRetriever()
        retriever.setDataSource(path)
        val width =
            Integer.valueOf(retriever.extractMetadata(MediaMetadataRetriever.METADATA_KEY_VIDEO_WIDTH))
        val height =
            Integer.valueOf(retriever.extractMetadata(MediaMetadataRetriever.METADATA_KEY_VIDEO_HEIGHT))
        retriever.release()
        Log.e(TAG, "getVideoResolution: "+width + height )

        return Resolution(width, height)
    }

    companion object {
        private val TAG = Mp4Composer::class.java.simpleName
    }
}